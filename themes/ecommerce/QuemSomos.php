        <p class="quem_somos">Recebendo expressiva influência da Corte Portuguesa, Diamantina constitui-se num dos principais centros culturais do Brasil Colônia. Até hoje, a cidade guarda nos costumes, nas tradições, no casario e nas artes, aqueles traços, que um dia a ligaram, estreitamente, a Portugal.</p>

        <p class="quem_somos">Paradoxalmente, Diamantina com todo o seu acervo histórico e cultural se situa numa das regiões mais pobres do País, o Vale do Jequitinhonha. As próprias características sócio-econômicas da região fizeram com que o surgimento do artesanato, sob as mais diversas manifefstações, se desse de maneira natural.</p>

        <p class="quem_somos">Compatibilizando-se a vocação artesanal do Vale com a herança artístico-cultural portuguesa, ainda viva na cidade, confere-se ao artesanato de tapetes Arraiolos de Diamantina o cunho de autenticidade que caracteriza as produções do gênero, principalmente por ele se constituir numas das formas mais belas de representação do folclore e da tradição de um povo.</p>

        <p class="quem_somos">Inspirado num ideal,  e sob a égide da Arquidiocese de Diamantina, o artesanato de tapetes Arraiolos foi estendido a 17 núcleos em todo o Vale do Jequitinhonha, reunindo cerca de 500 tapeceiras, inicialmente com o objetivo de melhorar as condições de vida das artesãs, por meio do aumento do nível de renda. A dimensão do projeto e o seu alcance social fizeram com que o Ministério do Trabalho apoiasse decisivamente a criação de uma cooperativa artesanal que, organizada sob modernas técnicas gerenciais, através do CEAG/MG, agente do Centro Brasileiro de Apoio à Pequena e Média Empresa, pudesse traduzir em benefícios às cooperadas, o produto de seu trabalho.</p>

