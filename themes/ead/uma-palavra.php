        <p>As Igrejas evangélicas, bem como também pessoas que anseiam por uma compreensão mais profunda da fé, carecem sempre de um instrumento, no qual possam melhor aprimorar seus conhecimentos teológicos entendendo com mais razão a natureza de sua confissão. Aproveitando-se de tal necessidade, surgem todos dias cursos que oferecem ajuda nessa vontade de compreender tais temas tão difíceis de interpretação. Embora haja muitos que tentam ajudar, pouco se tem progredido.</p>
        <p>Para dar sua contribuição em tal busca, apresentamos a todos os que anseiam por uma doutrina sadia, uma teologia com compromisso e seriedade, nossos serviços. Apresentamos o Cet-Rhema, que por quase duas décadas, tem oferecido cursos livre de teologia, com um bom número de obreiros já no campo, nas suas respectivas igrejas ajudando nos trabalhos da igreja local e muitos outros no ministério pastoral.</p>
        <p>Uma grande conquista foi alcançada pelos estudantes de teologia, quando o Ministério da Educação decidiu reconhecer os cursos de teologia, dando assim uma natureza mais acadêmica a essa ciência, que certamente trouxe grande contribuição aos que almejavam um curso de valor. Embora tenhamos que comemorar tal conquista, esse reconhecimento do MEC não trouxe nenhum benefício para o ministério exercido dentro das Igrejas, porque seu currículo não atende ao que o ministério da igreja exige.</p>
        <p>Diante de tal conjuntura, o curso livre de teologia oferecido pelos seminários tem como objetivo oferecer um ensino, que ao mesmo tempo prima por um dinâmica acadêmica, busca suprir aquelas necessidades próprias da vida da Igreja.</p>    
        <p>Conscientes de tal compromisso com as essas necessidades, com sua natureza espiritual da Igreja, o Cet-Rhema, traz sua contribuição na formação de obreiros e pastores que irão suprir tamanha necessidade na sua formação oferecendo cursos de excelente nível, tanto acadêmico quanto na área eclesiástica, onde sempre primamos pelo espiritual.</p>
        <p>A você que deseja estudar teologia de nível, em um seminário que tem compromisso com a verdade, BEM-VINDO AO CET-RHEMA, vamos iniciar uma grande caminhada.</p>
        <p>Lembrando aos interessados, que o Cet-Rhema já existe por quase vinte anos, já preparou um grande número de pastores, missionários e obreiros oriundos de várias denominações.</p>
        <p>Agora, para atender a necessidade daqueles que não podem estudar com um programa preso ao horário, o Cet-Rhema lança seus cursos no sistema, EAD, dando ao aluno condição de fazer seu tempo de estudo, sem perda da qualidade de ensino. A qualidade no ensino sistema EAD já é comprovada pelas universidades europeias, e no Brasil tem obtido grande sucesso.</p>
